// Componente de estrellas con rating
export function StarRating({ rating, reviews }: { rating: number; reviews: number }) {
  const fullStars = Math.floor(rating);
  const hasHalfStar = rating % 1 >= 0.5;

  return (
    <div className="flex items-center gap-2">
      <div className="flex">
        {[...Array(5)].map((_, i) => (
          <logos.Star
            key={i}
            className="size-5"
            filled={i < fullStars || (i === fullStars && hasHalfStar)}
          />
        ))}
      </div>
      <span className="text-sm font-semibold text-white">{rating}</span>
      <span className="text-sm text-gray-400">({reviews.toLocaleString()} reviews)</span>
    </div>
  );
}
